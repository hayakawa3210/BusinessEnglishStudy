const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
const metadataPath = path.join(root, 'js', 'app-metadata.js');
const packageJsonPath = path.join(root, 'package.json');

let version = '2.0.0';
if (fs.existsSync(packageJsonPath)) {
  try {
    const pkg = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'));
    if (pkg.version) {
      version = String(pkg.version);
    }
  } catch (error) {
    console.warn('package.json could not be read:', error.message);
  }
}

const now = new Date();
const lastUpdated = now.toLocaleString('ja-JP', {
  year: 'numeric', month: '2-digit', day: '2-digit',
  hour: '2-digit', minute: '2-digit', second: '2-digit',
  hour12: false,
  timeZone: 'Asia/Tokyo'
});

const content = `// Generated by script/gen-app-metadata.js
window.APP_VERSION = ${JSON.stringify(version)};
window.APP_LAST_UPDATED = ${JSON.stringify(lastUpdated)};
`;

fs.writeFileSync(metadataPath, content, 'utf8');
console.log(`Generated ${metadataPath}`);
console.log(`APP_VERSION=${version}`);
console.log(`APP_LAST_UPDATED=${lastUpdated}`);
